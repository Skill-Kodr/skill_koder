                <h4>  <strong>Does scoliosis always need surgery?</strong></h4>
                <p>When Cobb angle is more than 50 degrees, scoliosis almost always needs surgery. Curves less than 30 degrees usually do not need surgery. Braces like Milwaukee brace and Boston brace may be used for curves between 20 and 50 degrees. Curves more than 80 degrees can compromise the functioning of heart and lungs and should always be corrected with surgery. Corrective surgery for scoliosis is usually performed from the middle of the back. The curvature in the spine is corrected using titanium screws and rods and the bones involved in the curvature are then fused together so that the curve does not progress any further with growth</p>
