On an x-ray, the spine of a person with scoliosis looks more like an “S” or a “C” than a straight line. On Xrays, the severity of the scoliosis curve is measured by an angle called Cobb angle. For proper evaluation of scoliosis, your spine surgeon may order X-rays (AP and Lateral views) of Whole Spine (showing from neck to the hips in one film). Right and left bending Whole Spine X-rays and X-rays done under traction (pull) may be required if planning a surgery for Scoliosis. MRI of the Whole Spine will be required in all cases with Congenital Scoliosis and in Adolescent Idiopathic scoliosis where surgical correction is planned.</p></strong>
