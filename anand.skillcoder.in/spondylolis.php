                <p> 
         
                Pars interarticularis is a very strong pillar of bone which acts as a stabilizing staructure or lock between spinal bones. Sometimes, this pars interarticularis develops a break or defect due to which the spine loses its stability resulting in slippage of one bone on another. The reason for this pars defect may be injury, repeated injury over a period of time as in sportspersons / athletes or due to anatomical predisposition since birth. This type is most common at L5-S1.
               
                </p>
                <h4>How is Spondylolisthesis Surgery done?</h4>
                <p>This operation is done from the back. In this operation, titanium screws are inserted in 2 spinal bones and the disc between them is taken out in entirety while protecting the spinal nerves. Then, a plastic cage filled with bone graft is packed in the place of the disc so that over a period of time, bone grows between the two bones and they become fused (joined) to each other. This eliminates any further risk of slipping and relieves the patientâ€™s pain.</p>
            
               
