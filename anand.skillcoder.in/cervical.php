                <p>When a nerve root is pressed because of a slip disc or osteophytes in the neck, this condition is called cervical radiculopathy. These patients may complain of pain, tingling and numbness going down one of their arms. Rarely, they may also develop weakness of one arm. It is most common at C5-6 and C6-7 levels. If one is getting symptoms of nerve compression, then one must definitely meet a spinal surgeon. It can be easily diagnosed with Cervical Spine MRI.</p>
            
               
